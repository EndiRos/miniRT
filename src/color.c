#include "minirt.h"

unsigned int	rgb_to_rgba(t_RGB *rgb)
{
	return (((rgb->r >> 16) & 0xFF) << 24 | ((rgb->g >> 8) & 0xFF) << 16 | \
	(rgb->b & 0xFF) << 8 | 0xFF);
}